package Appliances;

public class Coordinates {
    private double x;
    private Double y; //Значение поля должно быть больше -748, Поле не может быть null


    public double getX() {
        return x;
    }
    public void setX(double x) {
        this.x = x;
    }


    public Double getY() {
        return y;
    }
    public void setY(Double y) {
        this.y = y;
    }
}